package com.example.healthyme_app.HelperClasses.ProteinAdapter;

public class Protein {

    int image;
    String title;

    public Protein(int image, String title) {
        this.image = image;
        this.title = title;

    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
